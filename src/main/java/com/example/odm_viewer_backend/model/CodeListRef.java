package com.example.odm_viewer_backend.model;

public class CodeListRef {

    private String codeListOID;
    private String methodOID;
    private String orderNumber;

    public String getCodeListOID() {
        return codeListOID;
    }

    public void setCodeListOID(String codeListOID) {
        this.codeListOID = codeListOID;
    }

    public String getMethodOID() {
        return methodOID;
    }

    public void setMethodOID(String methodOID) {
        this.methodOID = methodOID;
    }

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

}
